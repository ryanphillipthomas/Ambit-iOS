    override func tableView(_ tableView: UITableView, didSelectRowAt indexPath: IndexPath) {
        
        // Stop playing with a fade out
        currentSound?.stop()
        
        let row = indexPath.row
        switch row {
        case 0:
            currentSound = sound1
        case 1:
            currentSound = sound2
            
        default:
            break
        }
        
        currentSound?.currentTime = 0
        currentSound?.play()
    }

